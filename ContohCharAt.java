public class ContohCharAt {
    public static void main(String[] args) {
        String str = "Hello, Frisca!";
        char c1 = str.charAt(0);
        char c2 = str.charAt(7);
        System.out.println("Karakter pada indeks 0 adalah: " + c1);
        System.out.println("Karakter pada indeks 7 adalah: " + c2);
    }
}